# Question 3: Program to calculate Area of a Right Angled triangle base and height is given by user.

# Input Section

b = int(input("Enter the base of the triangle:"))
h = int(input("Enter the height of the triangle:"))

# Logic Section

area = 1/2*b*h

# Output Section

print("The area of given triangle with base",b,"and height",h,"is:",area)
